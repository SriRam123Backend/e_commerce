package models;

public class color{
	
	public String color;
	public String image;
	
	public color(String color, String image) {
		super();
		this.color = color;
		this.image = image;
	}

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}

	public String getImage() {
		return image;
	}

	public void setImage(String image) {
		this.image = image;
	}
	
   

}
